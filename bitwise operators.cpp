#include <iostream>
using namespace std;

int main()
{

    // And

    int a = 6;
    int b = 8;

    cout << "the value of a & b: " << (a & b) << endl;
    cout << "the value of a | b : " << (a | b) << endl;
    cout << "the value of  ~ b : " << ~b << endl;
    cout << "the value of a ^ b:  " << (a ^ b) << endl;
}